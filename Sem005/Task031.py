# Задача №31. Решение в группах
# Последовательностью Фибоначчи называется
# последовательность чисел a0
# , a1
# , ..., an
# , ..., где
# a0
#  = 0, a1
#  = 1, ak
#  = ak-1 + ak-2 (k > 1).
# Требуется найти N-е число Фибоначчи
# Input: 7
# Output: 21


def Fibonacci(n):
    if n in[1, 2]:
        return 1
    return Fibonacci(n - 2) + Fibonacci(n - 1)

print(Fibonacci(7))
